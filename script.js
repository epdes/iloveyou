function initials() {
  const first = loveLetter.yourName.trim().charAt(0) || "Y";
  const second = loveLetter.husbandName.trim().charAt(0) || "H";
  return `${first} & ${second}`;
}

function createDecorRow() {
  return `<div class="decor-row" aria-hidden="true"><span></span><span></span><span></span></div>`;
}

function paragraphs(lines = []) {
  return lines.map((line) => `<p>${line}</p>`).join("");
}

function photoMarkup(photoIndex) {
  const photo = loveLetter.photos[photoIndex] || loveLetter.photos[0];
  const frameClasses = ["photo-frame"];
  if (photo.wide) frameClasses.push("wide-photo");

  return `
    <figure class="${frameClasses.join(" ")}" style="--tilt: ${photoIndex % 2 === 0 ? "-2.5deg" : "2deg"}">
      <img src="${photo.src}" alt="${photo.alt}" style="object-position: ${photo.position || "center center"}">
      <figcaption class="missing-note">Replace with ${photo.src}</figcaption>
    </figure>
    <p class="caption">${photo.caption}</p>
  `;
}

function pageMarkup(page) {
  if (page.type === "cover") {
    return `
      <div class="page cover-page">
        <div class="cover-mark">
          <div class="monogram">${initials()}</div>
        </div>
        <p class="eyebrow">${loveLetter.dateLine}</p>
        <h1>${loveLetter.title}</h1>
        <p class="subtitle">${loveLetter.subtitle}</p>
        <p class="date-line">${loveLetter.yourName} to ${loveLetter.husbandName}</p>
      </div>
    `;
  }

  if (page.type === "photo") {
    return `
      <div class="page photo-page">
        <p class="eyebrow">${page.eyebrow}</p>
        <h2>${page.heading}</h2>
        ${photoMarkup(page.photoIndex)}
      </div>
    `;
  }

  if (page.type === "closing") {
    return `
      <div class="page closing-page">
        <p class="eyebrow">${page.eyebrow}</p>
        <h2>${page.heading}</h2>
        <div class="letter">${paragraphs(page.body)}</div>
        ${photoMarkup(page.photoIndex)}
        <p class="closing-signature">${page.signature}<br>${loveLetter.yourName}</p>
      </div>
    `;
  }

  return `
    <div class="page letter-page">
      <p class="eyebrow">${page.eyebrow}</p>
      <h2>${page.heading}</h2>
      ${createDecorRow()}
      <div class="letter">${paragraphs(page.body)}</div>
    </div>
  `;
}

function renderBook() {
  book.innerHTML = loveLetter.pages.map((page, index) => {
    const typeClass = page.type || "letter-sheet";
    return `<article class="sheet ${typeClass}" style="z-index: ${loveLetter.pages.length - index}" aria-label="Page ${index + 1}">${pageMarkup(page)}</article>`;
  }).join("");

  book.querySelectorAll(".photo-frame img").forEach((image) => {
    image.addEventListener("error", () => {
      image.closest(".photo-frame").classList.add("missing");
    });
  });
}

function updateControls() {
  const total = loveLetter.pages.length;
  readingStatus.textContent = `Page ${currentPage + 1} of ${total}`;

  prevPage.disabled = currentPage === 0 || isTurning;
  mobilePrev.disabled = prevPage.disabled;
  nextPage.disabled = currentPage >= total - 1 || isTurning;
  mobileNext.disabled = nextPage.disabled;
}

function finishTurn(sheet) {
  sheet.classList.remove("turning");
  isTurning = false;
  updateControls();
}

function goNext() {
  if (isTurning || currentPage >= loveLetter.pages.length - 1) return;

  const sheets = book.querySelectorAll(".sheet");
  const sheet = sheets[currentPage];
  isTurning = true;
  updateControls();
  sheet.classList.add("turning", "flipped");
  currentPage += 1;
  window.setTimeout(() => finishTurn(sheet), 980);
}

function goPrev() {
  if (isTurning || currentPage <= 0) return;

  const sheets = book.querySelectorAll(".sheet");
  currentPage -= 1;
  const sheet = sheets[currentPage];
  isTurning = true;
  updateControls();
  sheet.classList.add("turning");
  sheet.classList.remove("flipped");
  window.setTimeout(() => finishTurn(sheet), 980);
}

async function toggleAudio() {
  if (audio.paused) {
    try {
      await audio.play();
      audioToggle.classList.add("is-playing");
      audioToggle.setAttribute("aria-label", "Pause music");
    } catch (error) {
      readingStatus.textContent = "Add music.mp3";
      window.setTimeout(updateControls, 1600);
    }
  } else {
    audio.pause();
    audioToggle.classList.remove("is-playing");
    audioToggle.setAttribute("aria-label", "Play music");
  }
}

function toggleMute() {
  audio.muted = !audio.muted;
  muteToggle.classList.toggle("is-muted", audio.muted);
  muteToggle.setAttribute("aria-label", audio.muted ? "Unmute music" : "Mute music");
}

function handleKeydown(event) {
  if (event.key === "ArrowRight" || event.key === " ") {
    event.preventDefault();
    goNext();
  }

  if (event.key === "ArrowLeft") {
    event.preventDefault();
    goPrev();
  }
}

function handleTouchStart(event) {
  const touch = event.changedTouches[0];
  touchStartX = touch.clientX;
  touchStartY = touch.clientY;
}

function handleTouchEnd(event) {
  const touch = event.changedTouches[0];
  const deltaX = touch.clientX - touchStartX;
  const deltaY = touch.clientY - touchStartY;

  if (Math.abs(deltaX) < 42 || Math.abs(deltaX) < Math.abs(deltaY)) return;
  if (deltaX < 0) goNext();
  if (deltaX > 0) goPrev();
}

function init() {
  document.title = loveLetter.title;
  audio.src = loveLetter.music;
  renderBook();
  updateControls();

  nextPage.addEventListener("click", goNext);
  mobileNext.addEventListener("click", goNext);
  prevPage.addEventListener("click", goPrev);
  mobilePrev.addEventListener("click", goPrev);
  audioToggle.addEventListener("click", toggleAudio);
  muteToggle.addEventListener("click", toggleMute);
  window.addEventListener("keydown", handleKeydown);
  book.addEventListener("touchstart", handleTouchStart, { passive: true });
  book.addEventListener("touchend", handleTouchEnd, { passive: true });
}

init();
